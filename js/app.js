function limpiar(){
  display.textContent = "";
}
function resetear(){
  display.textContent = "0";
  a = 0;
  b = 0;
  oper = "";
}

function resolver(){
  var res = 0;
  switch(oper){
    case "+":
      res = parseFloat(a) + parseFloat(b);
      break;
    case "-":
        res = parseFloat(a) - parseFloat(b);
        break;
    case "*":
      res = parseFloat(a) * parseFloat(b);
      if ((res > 999999999)) {
        var cadena = String(res);
        var m = cadena.length;
        var inicio = 0;
        var fin    = 3;
        var restemp = cadena.substring(inicio, fin);
        res = (restemp/100) + "E+" + (m-1) ;
      }

      break;
    case "/":
      res = parseFloat(a) / parseFloat(b);
      var cadena = String(res);
      var inicio = 0;
      var fin    = 10;
      var restemp = cadena.substring(inicio, fin);
      res = Number(restemp);
      break;
  }
  resetear();
  display.textContent = res;
}
